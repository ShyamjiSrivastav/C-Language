#include<stdio.h>
int main()
{
    //Print Hello Students
    printf("Hello Students\n");

    //Print Hello in one line and Students in another line
    printf("Hello\nStudents\n");

    //print "MySirG" with double quotes
    printf("\"MySirG\"\n");

    //print /n
    printf("\\n\n");

    //print /r
    printf("\\r\n");
    
    //print "Teacher's Day" with double and single quotes
    printf("\"Teacher\'s Day\"\n");

    return 0;
}