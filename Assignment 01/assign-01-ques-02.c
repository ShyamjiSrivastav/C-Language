//Program to calculate the sum of two number given by user through keyboard
#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter first number = ");
    scanf("%d",&a);
    printf("Enter second number = ");
    scanf("%d",&b);
    printf("Sum of the %d and %d is %d",a,b,a+b);
    return 0;
}